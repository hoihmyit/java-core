package com.jc.hm.loops;

public class WhileLoopExample {
    public static void main(String[] args) {
        int i = 0;
        while (i < 5) {
            System.out.println("i = " + i);
            i++;
        }
        //i = 0
        //i = 1
        //i = 2
        //i = 3
        //i = 4
    }
}
