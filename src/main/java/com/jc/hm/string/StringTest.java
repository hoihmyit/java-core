package com.jc.hm.string;

public class StringTest {
    public static void main(String[] args) {

        // String type
        String str = "ABC";

        str = "XYZ";

        str += "123";

        System.out.println(str); // XYZ123

        // int type
        int i = 1;

        i = 2;

        System.out.println(i); // 2
    }
}
